#!/bin/bash
###############################################################################
#
#                           Kernel Build Script 
#
###############################################################################

##############################################################################
# set toolchain
##############################################################################
export ARCH=arm
export CROSS_COMPILE=~/{Your path}/arm-eabi-4.6/bin/arm-eabi-

##############################################################################
# make zImage
##############################################################################
mkdir -p ./obj/KERNEL_OBJ/
make O=./obj/KERNEL_OBJ ef47s_custom_defconfig
make -j20 O=./obj/KERNEL_OBJ

##############################################################################
# Copy Kernel Image
##############################################################################
if [ -f ./obj/KERNEL_OBJ/arch/arm/boot/zImage ]
then
    cp -f ./obj/KERNEL_OBJ/arch/arm/boot/zImage ./
fi

